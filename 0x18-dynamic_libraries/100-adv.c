

/**
 * add - add function
 * @a: 1st number
 * @b: second number
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub function
 * @a: 1st number
 * @b: second number
 * Return: a - b
 */
int sub(int a, int b)
{
        return (a - b);
}

/**
 * mul - mul function
 * @a: 1st number
 * @b: second number
 * Return: a * b
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - div function
 * @a: 1st number
 * @b: second number
 * Return: a / b
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * mod - mod function
 * @a: 1st number
 * @b: second number
 * Return: a / b
 */
int mod(int a, int b)
{
        return (a % b);
}

